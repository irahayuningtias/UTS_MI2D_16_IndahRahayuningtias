                        <hr class="divider divider-light" />
                        <p class="text-white-75 mb-4">Makam Bung Karno merupakan kompleks pemakaman presiden pertama Republik Indonesia, Soekarno.
                            Desain dari pemakaman ini dibangun dengan arsitektur khas Jawa, yaitu bangunan joglo. 
                            Kompleks tersebut terletak di Bendogerit, Sananwetan, Blitar, dan dibangun di akhir 1970-an. 
                            Ratusan ribu peziarah, baik rohani maupun politik, mengunjungi makam tersebut setiap tahun. 
                            Kompleks ini memiliki museum, perpustakaan nasional, teater, gong perdamaian, makam sang proklamator,
                            dan pasar oleh-oleh.<br><br>
                            Buka : Setiap Hari<br>
